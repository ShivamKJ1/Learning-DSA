#include<iostream>
using namespace std;

int main()
{
    int a;// declaration of integer variable in cpp
    a=12; // initialisation 

    cout<<"size of int "<<sizeof(a)<<endl;

    float b;
    cout<<"size of float "<<sizeof(b)<<endl;

    char c;
    cout<<"size of character "<<sizeof(c)<<endl;

    bool d;
    cout<<"size of boolean  "<<sizeof(d)<<endl;


    return 0;


}